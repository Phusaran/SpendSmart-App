/*
=========================================================================================
[สรุปภาพรวมการทำงานของคลาส LoginPage]
ไฟล์นี้คือ "โค้ดเบื้องหลังหน้าจอ UI (Code-Behind)" สำหรับหน้าจอเข้าสู่ระบบหรือตรวจสอบรหัสผ่าน (Login Screen)
ตามสถาปัตยกรรม MVVM หน้าจอนี้จะสะอาดและโล่งมาก เพราะไม่ต้องการให้ลอจิกความปลอดภัย (เช่น การตรวจรหัสผ่าน 
หรือการเรียกใช้ระบบสแกนลายนิ้วมือ/ใบหน้า) มาปนอยู่กับฝั่งหน้าจอ โดยคลาสนี้ทำหน้าที่สำคัญ 2 อย่างคือ:

1. รับระบบความปลอดภัยเข้ามาทำงาน (Dependency Injection): รับคลาส LoginViewModel เข้ามาทาง Constructor 
   เมื่อแอปพลิเคชันเปิดขึ้นมาและเรียกใช้งานหน้าจอด่านแรกนี้
2. ผูกสายไฟข้อมูล (Data Binding): กำหนด BindingContext เพื่อเชื่อมโยงปุ่มตัวเลข ปุ่มล็อกอิน หรือช่องกรอกรหัสใน XAML 
   ให้ส่งคำสั่งไปประมวลผลความปลอดภัยและนำทาง (Navigation) ใน ViewModel ได้ทันที
=========================================================================================
*/

using SpendSmart.ViewModels; // นำเข้าพื้นที่ทำงานของ ViewModels เพื่อดึง LoginViewModel มาใช้งาน

namespace SpendSmart.Views
{
    // กำหนดให้เป็น partial class เพื่อให้ระบบนำโค้ดส่วนนี้ไปประกอบรวมร่างกับหน้าจอดีไซน์ XAML (LoginPage.xaml) ตอนคอมไพล์แอป
    public partial class LoginPage : ContentPage
    {
        /// <summary>
        /// Constructor: ฟังก์ชันเริ่มต้นทำงานเมื่อแอปพลิเคชันเปิดตัวขึ้นมาที่หน้าจอล็อกอิน
        /// มีการใช้ระบบ Dependency Injection (DI) ในการส่ง LoginViewModel (สมองกลตรวจสอบรหัส) เข้ามาให้โดยอัตโนมัติ
        /// </summary>
        public LoginPage(LoginViewModel viewModel)
        {
            InitializeComponent(); // คำสั่งบังคับของ .NET MAUI เพื่อสั่งให้ระบบวาดชิ้นส่วน UI ทั้งหมด (เช่น ช่องกรอก PIN, ปุ่มสแกน) จาก XAML

            // 🌟 จุดสลักสำคัญ: ผูก BindingContext ของหน้าจอนี้เข้ากับสมองกล ViewModel
            // บรรทัดนี้จะทำให้องค์ประกอบในหน้า XAML (เช่น ปุ่มกดรหัสผ่าน หรือกล่องข้อความเตือนรหัสผิด) 
            // สามารถเชื่อมต่อ ดึงข้อมูล และส่งคำสั่งตรวจสอบความปลอดภัยไปยัง LoginViewModel ได้โดยตรง
            BindingContext = viewModel;
        }
    }
}