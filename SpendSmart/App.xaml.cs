namespace SpendSmart
{
    // สืบทอดคุณสมบัติมาจากคลาส Application ของ .NET MAUI เพื่อทำหน้าที่ควบคุมวงจรชีวิตแอป (เปิด, ปิด, พักแอป)
    public partial class App : Application
    {
        /// <summary>
        /// Constructor: ฟังก์ชันเริ่มต้นทำงานก้าวแรกสุดของแอปพลิเคชัน SpendSmart
        /// มีการรับตัวแปร serviceProvider (กล่องเครื่องมือรวมบริการทั้งหมดของแอป) เข้ามาใช้งาน
        /// </summary>
        public App(IServiceProvider serviceProvider)
        {
            InitializeComponent(); // คำสั่งบังคับของระบบ เพื่อทำการรีดสไตล์, สีธีมทอง-ขาว และทรัพยากรส่วนกลางจากไฟล์ App.xaml ออกมาใช้งาน

            // 🌟 จุดสลักสำคัญ: การกำหนดโครงสร้างหน้าแรก (Root Page Setting)
            // 
            // ลอจิก: สั่งคำสั่ง GetService เพื่อไปล้วงดึงหน้าจอ Views.LoginPage ออกมาจากกล่องเซ็นเตอร์ (DI Container) 
            // ที่เราได้ลงทะเบียนผูกสายไฟจับคู่กับ ViewModel ไว้เรียบร้อยแล้วในไฟล์ MauiProgram.cs
            // จากนั้นยัดใส่ตัวแปร MainPage เพื่อบังคับให้แอปพลิเคชันเปิดหน้านี้ขึ้นมาสบตาผู้ใช้เป็นด่านแรกสุดครับ
            MainPage = serviceProvider.GetService<Views.LoginPage>();
        }
    }
}