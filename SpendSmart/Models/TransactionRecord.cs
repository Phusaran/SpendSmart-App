/*
=========================================================================================
[สรุปภาพรวมการทำงานของคลาส TransactionRecord]
ไฟล์นี้คือ "โครงสร้างตารางฐานข้อมูล (Database Model)" สำหรับเก็บประวัติ "การทำธุรกรรม" ทั้งหมดในแอป
ใช้สำหรับบันทึกว่า ผู้ใช้จ่ายเงินไปเท่าไหร่, วันไหน, หมวดอะไร และดึงเงินมาจากกระเป๋าใบไหน

ความฉลาดของการออกแบบคลาสนี้คือ การแบ่งตัวแปรออกเป็น 2 กลุ่ม:
1. กลุ่มที่บันทึกลงฐานข้อมูลจริง (เช่น Amount, Date, PocketId)
2. กลุ่มที่ใช้แสดงผลบนหน้าจอเท่านั้น (มีคำสั่ง [Ignore] กำกับไว้) เช่น AmountColor ที่ช่วยคำนวณ
   ว่ายอดเงินนี้ควรโชว์เป็นสีเขียว (รายรับ) หรือสีแดง (รายจ่าย) เพื่อให้หน้าจอ UI ดึงไปใช้วาดได้ทันที
=========================================================================================
*/

using SQLite;                    // เครื่องมือสำหรับสร้างและจัดการตารางฐานข้อมูล

namespace SpendSmart.Models
{
    /// <summary>
    /// คลาสตัวแทนข้อมูล "ประวัติการทำธุรกรรม" 1 รายการ
    /// </summary>
    public class TransactionRecord
    {
        // 1. รหัสประจำรายการ (Transaction ID)
        // [PrimaryKey] บังคับให้เป็นรหัสหลัก ห้ามซ้ำกัน
        // [AutoIncrement] ให้ฐานข้อมูลรันเลขรหัสให้เองอัตโนมัติเมื่อมีการเพิ่มรายการใหม่
        [PrimaryKey, AutoIncrement]
        public int Id { get; set; }

        // 2. จำนวนเงินสุทธิของการทำธุรกรรมครั้งนี้
        public decimal Amount { get; set; }

        // 3. ประเภทของรายการ (รองรับ 3 แบบ: "Expense" = รายจ่าย, "Income" = รายรับ, "โยกเงิน" = โอนหากัน)
        public string Type { get; set; }

        // 4. หมวดหมู่ย่อยของการใช้จ่าย (เช่น "อาหาร", "เดินทาง", "ช้อปปิ้ง")
        public string SubCategory { get; set; }

        // 5. วันและเวลาที่เกิดการทำธุรกรรมขึ้น
        public DateTime Date { get; set; }

        // 6. บันทึกช่วยจำ หรือ โน้ตรายละเอียด (เช่น "กินชาบู", "เงินเดือนออก")
        public string Note { get; set; }

        // 7. รหัสกระเป๋าเงิน (Pocket ID) ที่รายการนี้ไปหัก/เพิ่มเงิน
        // [Indexed] คือการทำดัชนี ช่วยให้เวลาค้นหาว่า "กระเป๋าใบนี้มีประวัติอะไรบ้าง" ฐานข้อมูลจะหาได้เร็วขึ้นมาก
        [Indexed]
        public int PocketId { get; set; }

        // 8. ที่อยู่ไฟล์รูปภาพสลิปโอนเงิน (Path) ในกรณีที่ผู้ใช้สแกนสลิปหรือแนบรูปมาด้วย
        public string ReceiptImagePath { get; set; }

        // 9. รหัสกระเป๋าปลายทาง (ใช้เฉพาะกรณี Type = "โยกเงิน" เพื่อให้รู้ว่าโอนจาก PocketId ไปหา TargetPocketId)
        public int TargetPocketId { get; set; }

        // ==========================================================================
        // 🌟 กลุ่มตัวแปรสำหรับแสดงผล (UI Helpers) ที่จะไม่ถูกเซฟลงฐานข้อมูล
        // [Ignore] คือคำสั่งบอก SQLite ว่า "ไม่ต้องเอาตัวแปรนี้ไปสร้างเป็นคอลัมน์ในตารางนะ"
        // ==========================================================================

        // 10. จัดฟอร์แมตตัวเลขยอดเงินให้มีทศนิยม 2 ตำแหน่ง และเติมเครื่องหมาย ฿ ต่อท้าย (เช่น "500.00 ฿")
        [Ignore]
        public string AmountDisplay
        {
            get
            {
                return $"{Amount:N2} ฿";
            }
        }

        // 11. กำหนดสีของตัวเลขยอดเงินแบบอัตโนมัติตามประเภทรายการ
        [Ignore]
        public Color AmountColor
        {
            get
            {
                // ถ้ารายรับ = สีเขียว
                if (Type == "Income")
                    return Color.FromArgb("#27AE60");

                // ถ้าเป็นการโยกเงินระหว่างกระเป๋า = สีฟ้า
                if (Type == "โยกเงิน")
                    return Color.FromArgb("#3498DB");

                // ถ้ารายจ่าย (และอื่นๆ) = สีแดง
                return Color.FromArgb("#E74C3C");
            }
        }

        // 12. ตัวแปรเช็กว่ารายการนี้ "มีรูปสลิป" แนบมาด้วยหรือไม่ (คืนค่า True ถ้ามีรูป)
        [Ignore]
        public bool HasReceipt => !string.IsNullOrWhiteSpace(ReceiptImagePath);

        // 13. ตัวแปรเช็กว่ารายการนี้ "ไม่มีรูปสลิป" ใช่หรือไม่ (คืนค่า True ถ้าไม่มีรููป)
        // (ตัวแปรนี้เอาไว้ผูกกับ UI เพื่อสั่งซ่อนไอคอนสลิปบนหน้าจอ)
        [Ignore]
        public bool NoReceipt => string.IsNullOrWhiteSpace(ReceiptImagePath);
    }
}