using SQLite; // เรียกใช้เครื่องมือ SQLite เพื่อจำลองคลาสนี้เป็นตารางในฐานข้อมูล

namespace SpendSmart.Models
{
    /// <summary>
    /// คลาสตัวแทนข้อมูล "โปรไฟล์และระบบเลเวลสะสมแต้ม" ของผู้ใช้งาน
    /// </summary>
    public class UserProfile
    {
        // 1. รหัสประจำตัวโปรไฟล์
        // [PrimaryKey] กำหนดให้เป็นรหัสหลัก
        // ตั้งค่าเริ่มต้นไว้ที่ = 1 เสมอ เพื่อบังคับให้แอปนี้มีผู้ใช้ผูกเลเวลได้แค่ 1 คนต่อ 1 เครื่อง
        [PrimaryKey]
        public int Id { get; set; } = 1;

        // 2. แต้มประสบการณ์รวมทั้งหมด (Total EXP) ที่สะสมมาตั้งแต่เริ่มออมเงิน/จดบันทึก
        // ตัวแปรนี้จะเพิ่มทีละ +10 ทุกครั้งที่ผู้ใช้กดบันทึกรายรับ-รายจ่ายสำเร็จ
        public int TotalExp { get; set; }

        // ==========================================================================
        // 🌟 กลุ่มตัวแปรคำนวณอัตโนมัติ (Computed Properties) สำหรับคำนวณเลเวลและส่งให้ UI
        // ==========================================================================

        // 3. คำนวณเลเวลปัจจุบัน (Level) 
        // ลอจิก: ทุกๆ 10 แต้มจะได้เลเวลอัป 1 เลเวล (เช่น มี 25 แต้ม ได้เลเวล 2)
        // ใช้ Math.Max(1, ...) ดักเซฟตี้ไว้ เพื่อให้เลเวลเริ่มต้นที่เลเวล 1 เสมอ แม้แต้มจะเป็น 0
        public int CurrentLevel => Math.Max(1, TotalExp / 10);

        // 4. คะแนนที่ใช้นำไปโชว์บนหลอดพลัง 
        // ลอจิก: ใช้ Math.Min เพื่อจำกัดไม่ให้แต้มแสดงผลเกิน 50 EXP (ซึ่งเป็นเป้าหมายสูงสุดของรางวัลชิ้นสุดท้าย)
        public int DisplayExp => Math.Min(TotalExp, 50);

        // 5. คำนวณเพดานคะแนนเป้าหมาย (ExpCap) สำหรับใช้เป็นตัวหารในแต่ละระดับรางวัล
        // ลอจิกแบบขั้นบันได: 
        // - ถ้าแต้มยังไม่ถึง 30 EXP เป้าหมายหลอดพลังจะตั้งไว้ที่ 30 (เพื่อปลดล็อกฟีเจอร์ AI Chat)
        // - ถ้าแต้มเกิน 30 EXP ไปแล้ว เป้าหมายหลอดพลังจะขยับขึ้นเป็น 50 (เพื่อปลดล็อกฟีเจอร์ AI วิเคราะห์รายเดือน)
        public int ExpCap
        {
            get
            {
                if (TotalExp < 30)
                    return 30;

                return 50;
            }
        }

        // 6. คำนวณความคืบหน้าของหลอดพลัง (ส่งค่า 0.0 ถึง 1.0 ไปให้คอนโทรล Progressbar ในหน้า XAML)
        // ลอจิก: นำแต้มที่แสดงผล (DisplayExp) หารด้วย เพดานเป้าหมาย (ExpCap) 
        // และใช้ Math.Min(1.0, ...) ดักไว้ เพื่อไม่ให้หลอดพลังวิ่งทะลุเกิน 100% เมื่อเก็บแต้มครบ
        public double LevelProgress
        {
            get
            {
                if (ExpCap <= 0)
                    return 0;

                return Math.Min(1.0, (double)DisplayExp / ExpCap);
            }
        }

        // 7. จัดรูปแบบข้อความแสดงผลคะแนนบนหน้าจอ UI (เช่น "15 / 30 EXP" หรือ "45 / 50 EXP")
        public string ExpText => $"{DisplayExp} / {ExpCap} EXP";
    }
}