ไฟล์นี้คือ "โครงสร้างตารางฐานข้อมูล (Database Model)" สำหรับเก็บข้อมูล "โปรไฟล์และเลเวลของผู้ใช้"
โดยระบุข้อจำกัดให้มีข้อมูลได้เพียงแค่ 1 แถวในฐานข้อมูลเสมอ (Id = 1) เพื่อใช้จำแต้มของผู้ใช้งานเครื่องนี้

ความฉลาดของคลาสนี้คือ การใช้ลอจิกคำนวณสด (Computed Properties) เพื่อเปลี่ยนแต้มประสบการณ์ดิบ (TotalExp)
ให้กลายเป็น:
1. เลเวลปัจจุบัน (CurrentLevel) แบบเลเวลอัปอัตโนมัติทุกๆ 10 EXP
2. เพดานคะแนน (ExpCap) แบบขั้นบันได ขยับจากเป้าหมายแรก 30 EXP ไปยังเป้าหมายถัดไปที่ 50 EXP
3. เปอร์เซ็นต์หลอดพลัง (LevelProgress) สำหรับส่งค่าทศนิยม (0.0 - 1.0) ให้หน้าจอ UI นำไปวิ่งหลอด EXP สวยๆ
