namespace SpendSmart.Models
{
    /// <summary>
    /// คลาสตัวแทนข้อมูลสรุปรายจ่ายแบบกลุ่ม (ตัวอย่างเช่น กล่องเก็บข้อมูลหมวด "อาหาร")
    /// </summary>
    public class CategorySummary
    {
        // 1. ชื่อหมวดหมู่ (เช่น "อาหาร", "เดินทาง", "ช้อปปิ้ง")
        public string CategoryName { get; set; }

        // 2. ยอดเงินรวมของหมวดหมู่นี้ในเดือนนั้นๆ (เช่น 1500.00 บาท)
        // (ใช้ decimal เพราะเป็นข้อมูลประเภทตัวเลขการเงิน ต้องการความแม่นยำสูง)
        public decimal Amount { get; set; }

        // 3. สัดส่วนเปอร์เซ็นต์เมื่อเทียบกับรายจ่ายทั้งหมด (เช่น 45.5%)
        // (ใช้ double เพื่อเก็บค่าทศนิยมสำหรับการนำไปวาดความกว้างของกราฟวงกลม)
        public double Percentage { get; set; }
    }
}