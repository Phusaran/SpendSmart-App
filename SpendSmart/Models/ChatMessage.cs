namespace SpendSmart.Models
{
    /// <summary>
    /// คลาสตัวแทนข้อมูลของ "ฟองสบู่แชท 1 กล่อง" บนหน้าจอ
    /// </summary>
    public class ChatMessage
    {
        // 1. เก็บเนื้อหาข้อความแชทจริงๆ 
        public string Text { get; set; }

        // 2. สวิตช์บอกสถานะว่าข้อความนี้ "ผู้ใช้เป็นคนพิมพ์" ใช่หรือไม่?
        // (ถ้าเป็น True = ผู้ใช้พิมพ์, ถ้าเป็น False = AI เป็นคนตอบ)
        public bool IsUser { get; set; }

        // ==========================================================================
        // 🌟 กลุ่มตั้งค่า UI อัตโนมัติ (Computed Properties)
        // ทำงานโดยอ้างอิงจากตัวแปร IsUser ด้านบน (ใช้แบบ Arrow Function => เพื่อลดรูปโค้ด)
        // ==========================================================================

        // 3. กำหนดชื่อคนส่ง: ถ้าผู้ใช้ส่งให้ขึ้นว่า "คุณ" แต่ถ้า AI ตอบให้ขึ้นว่า "SpendSmart AI"
        public string SenderName => IsUser ? "คุณ" : "SpendSmart AI";

        // 4. กำหนดสีพื้นหลังฟองสบู่แชท: 
        // ผู้ใช้ส่ง = สีทอง (#D4AF37)
        // AI ตอบ = สีขาว (#FFFFFF)
        public Color BackgroundColor => IsUser ? Color.FromArgb("#D4AF37") : Color.FromArgb("#FFFFFF");

        // 5. กำหนดสีตัวอักษร:
        // ฟองสบู่สีทองของผู้ใช้ = ตัวอักษรสีขาว (Colors.White) จะได้อ่านง่าย
        // ฟองสบู่สีขาวของ AI = ตัวอักษรสีกรมท่า/เทาเข้ม (#2C3E50) 
        public Color TextColor => IsUser ? Colors.White : Color.FromArgb("#2C3E50");

        // 6. กำหนดตำแหน่งการจัดวางฟองสบู่บนหน้าจอ (Alignment):
        // ผู้ใช้ส่ง = ชิดขวา (LayoutOptions.End) 
        // AI ตอบ = ชิดซ้าย (LayoutOptions.Start) เหมือนแอป Line หรือ Messenger
        public LayoutOptions Alignment => IsUser ? LayoutOptions.End : LayoutOptions.Start;
    }
}