using SQLite; // เครื่องมือสำหรับสร้างและจัดการตารางฐานข้อมูล

namespace SpendSmart.Models
{
    /// <summary>
    /// คลาสตัวแทนข้อมูล "เป้าหมายการออมเงิน" 1 เป้าหมาย
    /// </summary>
    public class FinancialGoal
    {
        // 1. รหัสอ้างอิง (ID) ของเป้าหมาย
        // [PrimaryKey] = กำหนดให้คอลัมน์นี้เป็นรหัสหลัก ห้ามซ้ำกัน
        // [AutoIncrement] = ให้ระบบฐานข้อมูลรันตัวเลขรหัสให้เองอัตโนมัติ (เช่น 1, 2, 3...)
        [PrimaryKey, AutoIncrement]
        public int Id { get; set; }

        // 2. ชื่อเป้าหมาย (เช่น "เก็บเงินซื้อ iPhone 16", "ทริปเที่ยวญี่ปุ่น")
        public string Title { get; set; }

        // 3. จำนวนเงินเป้าหมายสูงสุดที่ต้องเก็บให้ถึง (เช่น 40,000 บาท)
        public decimal TargetAmount { get; set; }

        // 4. จำนวนเงินที่เก็บสะสมได้ในปัจจุบัน (เช่น ตอนนี้เก็บได้แล้ว 15,000 บาท)
        public decimal CurrentSavedAmount { get; set; }

        // 5. วันที่กำหนดเส้นตาย หรือวันที่คาดว่าจะเก็บเงินสำเร็จ
        public DateTime TargetDate { get; set; }

        // 6. จำนวนเงินที่ระบบแนะนำให้เก็บ "ต่อเดือน" เพื่อให้ทันเวลา (ได้มาจากการคำนวณ)
        public decimal RecommendedMonthlySaving { get; set; }
    }
}