using SQLite; // เรียกใช้เครื่องมือ SQLite เพื่อจำลองคลาสนี้เป็นตารางในฐานข้อมูล

namespace SpendSmart.Models
{
    /// <summary>
    /// คลาสตัวแทนข้อมูล "กระเป๋าเงิน" 1 ใบ
    /// </summary>
    public class Pocket
    {
        // 1. รหัสประจำตัวกระเป๋า (ID)
        // [PrimaryKey] = บังคับให้เป็นรหัสหลัก ห้ามซ้ำกัน
        // [AutoIncrement] = ให้ฐานข้อมูลรันเลขรหัสให้เองอัตโนมัติ (1, 2, 3...)
        [PrimaryKey, AutoIncrement]
        public int Id { get; set; }

        // 2. ชื่อกระเป๋า (เช่น "เงินเดือน", "เที่ยวญี่ปุ่น", "ค่าอาหาร")
        public string Name { get; set; }

        // 3. 🌟 ประเภทของกระเป๋า: 
        // ใช้แยกว่าเป็น "Saving" (สำหรับออมเงิน) หรือ "Spending" (สำหรับกดจ่ายใช้งานทั่วไป)
        // ค่าเริ่มต้น (Default) ถูกตั้งให้เป็น "Saving"
        public string PocketType { get; set; } = "Saving";

        // 4. งบประมาณเป้าหมาย หรือเพดานการใช้จ่าย
        public decimal TargetBudget { get; set; }

        // 5. ยอดเงินคงเหลือปัจจุบันในกระเป๋าใบนี้ (จะถูกหัก/เพิ่ม ตลอดเวลาที่มีการโอนเงิน)
        public decimal CurrentBalance { get; set; }

        // 6. ไอคอนประจำกระเป๋า (ค่าเริ่มต้นเป็นอีโมจิรูปถุงเงิน 💰)
        public string Icon { get; set; } = "💰";

        // 7. สีธีมประจำกระเป๋า (ค่าเริ่มต้นเป็นสีฟ้า Cyan #00E5FF)
        public string ThemeColor { get; set; } = "#00E5FF";

        // 8. สวิตช์บอกว่าเป็น "กระเป๋าหลัก" หรือไม่? (ถ้าเป็น True จะถูกเลือกอัตโนมัติเวลาเพิ่มรายการ)
        public bool IsDefault { get; set; }

        // 9. วันที่เป้าหมาย (สำหรับกระเป๋าเงินเก็บ) 
        // ใส่เครื่องหมาย ? หมายถึงตัวแปรนี้ "อนุญาตให้เป็นค่าว่าง (Null) ได้" 
        // เพราะถ้าเป็นกระเป๋าใช้จ่ายปกติ จะไม่มีวันที่เป้าหมาย
        public DateTime? TargetDate { get; set; }

        // 10. ยอดเงินที่ระบบคำนวณให้ว่าต้องออม "ต่อเดือน" เพื่อให้ถึงเป้าหมาย
        public decimal MonthlySavingAim { get; set; }

        // 11. ยอดเงินเป้าหมายสูงสุดที่ต้องการจะเก็บให้ถึง
        public decimal GoalAmount { get; set; }

        // ==========================================================================
        // 🌟 กลุ่มตัวแปรคำนวณอัตโนมัติ (Computed Properties) สำหรับส่งให้หน้าจอ UI วาดกราฟิก
        // ==========================================================================

        // 12. คำนวณเปอร์เซ็นต์ความคืบหน้า (Progress) เพื่อเอาไปแสดงเป็นหลอดพลังบนหน้าจอ
        // ลอจิก: ถ้ามียอดเป้าหมาย (GoalAmount > 0) ให้เอา เงินปัจจุบัน ÷ เงินเป้าหมาย (ได้ค่า 0.0 - 1.0)
        // แต่ถ้าเป็นกระเป๋าใช้จ่าย (เป้าหมาย = 0) ให้หลอดความคืบหน้าเป็น 0 (ซ่อนหลอด)
        public double Progress => GoalAmount > 0 ? (double)(CurrentBalance / GoalAmount) : 0;

        // 13. คำนวณข้อความที่จะแสดงบนหลอดความคืบหน้า
        // ลอจิก: ถ้ามียอดเป้าหมาย ให้ขึ้นเลข "ปัจจุบัน / เป้าหมาย" (เช่น ฿5,000 / ฿10,000)
        // แต่ถ้าเป็นกระเป๋าใช้จ่ายทั่วไป ให้แสดงข้อความว่า "กระเป๋าสำหรับใช้จ่าย" แทน
        public string ProgressText => GoalAmount > 0 ? $"฿{CurrentBalance:N0} / ฿{GoalAmount:N0}" : "กระเป๋าสำหรับใช้จ่าย";
    }
}