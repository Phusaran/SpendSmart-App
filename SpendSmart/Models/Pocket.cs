ความเจ๋งของคลาสนี้คือ มันไม่ได้เก็บแค่ข้อมูลดิบๆ (เช่น ชื่อ, ยอดเงิน) แต่มันมีการฝังลอจิก (Logic)
เพื่อแยกแยะว่ากระเป๋าใบนี้เป็น "กระเป๋าเงินเก็บ (Saving)" หรือ "กระเป๋าใช้จ่าย (Spending)" 
